//criar um programa com uma condicional composta para verificar a temperatura entre "frio", "agradavel" ou "quente"
let clima = 16
if(clima <= 15){
    console.log('o clima está frio')
}else if (clima >= 16 && clima <= 25){
    console.log('o clima está agradavel')
}else{
    console.log('o clima está quente')
}